export function getProductsWithCategories(): Product[] {
  return products.map(product => ({
    ...product,
    category: categories.find(c => c.id === product.categoryId),
  }));
}

export function getMovementsWithProducts(): Movement[] {
  return movements.map(movement => ({
    ...movement,
    product: products.find(p => p.id === movement.productId),
  }));
}

export function getDashboardStats() {
  const lowStockItems = products.filter(p => p.quantity <= p.reorderPoint).length;
  const totalValue = products.reduce((sum, p) => sum + (p.price * p.quantity), 0);
  
  return {
    totalProducts: products.length,
    totalValue,
    lowStockItems,
    totalCategories: categories.length,
  };
}

export function getStockByCategory() {
  return categories.map(category => {
    const categoryProducts = products.filter(p => p.categoryId === category.id);
    const totalStock = categoryProducts.reduce((sum, p) => sum + p.quantity, 0);
    return {
      name: category.name,
      stock: totalStock,
    };
  });
}
